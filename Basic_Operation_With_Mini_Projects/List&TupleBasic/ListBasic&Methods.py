# Basic Of lists 

list = [3,2,4]

#append()
list.append(1)
print(list)

#sort()
list.sort()
print(list)

#sort in desecnding
list.sort(reverse= True)
print(list)

#reversing the list
list.reverse()
print(list)

#Inserting on specific index
list.insert(2 , 5)
print(list)

#remove
list.remove(1)
print(list)

#pop
list.pop(3)
print(list)
